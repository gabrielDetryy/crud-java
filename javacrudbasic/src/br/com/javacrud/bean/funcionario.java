package br.com.javacrud.bean;

/**
 *
 * @author detry
 */
public class funcionario {
    
    
  int id ;
  String nome;
  int  idade;
  String fun;
  
  
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public int getIdade() {
	return idade;
}
public void setIdade(int idade) {
	this.idade = idade;
}
public String getFun() {
	return fun;
}
public void setFun(String fun) {
	this.fun = fun;
}
    
}
